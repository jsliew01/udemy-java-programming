public class WholeNumbers {
    public static void main(String[] args) {
        int numberOfFans = 2000;
        System.out.println("There are " + numberOfFans + " fans at the Quidditch games");

        long globalPopulation = 7000000000L;
        System.out.println("There are "+ globalPopulation + " people in the world");

        long googleSearches = 5000000000L;
        System.out.println("There are " + googleSearches + " daily google searches");
    }
    
}
